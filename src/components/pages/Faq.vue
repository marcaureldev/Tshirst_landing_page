function AfficheElement(bouton, accordeon, image,closebtn)
{
    document.addEventListener('DOMContentLoaded', ()=> {
    bouton.addEventListener( "click", ()=> {
        if (accordeon.classList.contains('hidden'))
        {
            accordeon.classList.remove('hidden')
            bouton.style.background = '#2EBB77'
            bouton.style.color = '#fff'
            bouton.innerHTML = 'How does product customization work?'
            bouton.appendChild(closebtn)
        }
        else
        {
            accordeon.classList.add('hidden')
            bouton.style.background = ''
            bouton.style.color = ''
            bouton.innerHTML = 'How does product customization work?'
            bouton.appendChild(image)
        }
    })

})
}

